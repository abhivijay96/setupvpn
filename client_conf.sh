#!/bin/bash

# First argument: Client identifier

mkdir ./clients/files

KEY_DIR=./clients/keys
OUTPUT_DIR=./clients/files
BASE_CONFIG=./configs/base.conf

sudo chmod a+r ${KEY_DIR}/ca.crt

cat ${BASE_CONFIG} \
    <(echo -e '<ca>') \
    ${KEY_DIR}/ca.crt \
    <(echo -e '</ca>\n<cert>') \
    ${KEY_DIR}/${1}.crt \
    <(echo -e '</cert>\n<key>') \
    ${KEY_DIR}/${1}.key \
    <(echo -e '</key>\n<tls-auth>') \
    ${KEY_DIR}/ta.key \
    <(echo -e '</tls-auth>') \
    > ${OUTPUT_DIR}/${1}.ovpn
